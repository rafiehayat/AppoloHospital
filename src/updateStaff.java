    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String staffId = jTextField1.getText();
        
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from addStaff where id ='"+staffId+"'");
            if(rs.next())
            {
              jTextField2.setText(rs.getString(2));
              jTextField9.setText(rs.getString(3));
              jTextField3.setText(rs.getString(4));
              jTextField4.setText(rs.getString(5));
              jTextField5.setText(rs.getString(6));
              jTextField6.setText(rs.getString(7));
            }else{
                JOptionPane.showMessageDialog(null, "Pharmacist does not exist");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

