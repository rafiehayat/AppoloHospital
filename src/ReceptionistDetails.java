    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String)jComboBox1.getSelectedItem();
        String address = jTextField3.getText();
        String specification = jTextField4.getText();
        String age = jTextField5.getText();
        String contact = jTextField6.getText();
        
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into ReceptionistDetails values('"+id+"' , '"+name+"' , '"+gender+"' "
             + ", '"+address+"' , '"+specification+"' , '"+age+"' , '"+contact+"' )");
            JOptionPane.showMessageDialog(this, "Added Successfully");
            new ReceptionistDetails().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

